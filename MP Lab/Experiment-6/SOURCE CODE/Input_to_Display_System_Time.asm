.MODEL SMALL
.STACK 100H
DATA SEGMENT
	M DB 'CURRENT SYSTEM TIME : $'
	T DB '00:00:00$'
DATA ENDS

CODE SEGMENT
	MAIN PROC
		MOV AX, @DATA
		MOV DS, AX
		LEA BX, T
		CALL TIME
		LEA DX, M
		MOV AH, 09H
		INT 21H
		LEA DX, T
		MOV AH, 09H
		INT 21H
		MOV AH, 4CH
		INT 21H
	MAIN ENDP

	TIME PROC
		PUSH AX
		PUSH CX
		MOV AH, 2CH
		INT 21H
		MOV AL, CH
		CALL FILLY
		MOV [BX], AX
		MOV AL, CL
		CALL FILLY
		MOV [BX+3], AX
		MOV AL, DH
		CALL FILLY
		MOV [BX+6], AX
		POP CX
		POP AX
		RET
	TIME ENDP

	FILLY PROC
		PUSH DX
		MOV AH, 0
		MOV DL, 10
		DIV DL
		OR AX, 3030H
		POP DX
		RET
	FILLY ENDP
END MAIN